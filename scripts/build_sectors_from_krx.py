def fetch_kospi_stocks() -> pd.DataFrame:
    """KIND에서 KOSPI 상장법인 목록 다운로드"""
    url = "https://kind.krx.co.kr/corpgeneral/corpList.do"
    params = {"method": "download", "searchType": "13"}
    headers = {
        "User-Agent": "Mozilla/5.0",
        "Referer": "https://kind.krx.co.kr/corpgeneral/corpList.do",
    }
    res = requests.get(url, params=params, headers=headers, timeout=20)
    res.raise_for_status()
    df = pd.read_html(io.BytesIO(res.content), encoding="euc-kr")[0]

    # 유가증권(KOSPI)만 필터
    kospi = df[df["시장구분"] == "유가"].copy()

    # 종목코드: 숫자 6자리만 유효 (SPAC, ETF 등 특수코드 제외)
    kospi["종목코드"] = kospi["종목코드"].astype(str).str.zfill(6)
    kospi = kospi[kospi["종목코드"].str.match(r"^\d{6}$")].copy()

    return kospi[["회사명", "종목코드", "업종"]].reset_index(drop=True)


def map_to_krx_sector(ksic_name: str) -> str:
    """KSIC 업종명 → KRX 섹터명 변환. 직접 매핑 없으면 키워드 기반 추론."""
    if ksic_name in KSIC_TO_KRX:
        return KSIC_TO_KRX[ksic_name]

    # 키워드 기반 fallback
    name = str(ksic_name)
    if any(k in name for k in ["은행", "저축"]):
        return "은행"
    if any(k in name for k in ["보험"]):
        return "보험"
    if any(k in name for k in ["증권", "자본시장", "투자자문", "집합투자"]):
        return "증권"
    if any(k in name for k in ["금융"]):
        return "금융업"
    if any(k in name for k in ["의약품", "바이오", "의료용 물질"]):
        return "의약품"
    if any(k in name for k in ["전자", "반도체", "전기장비", "전지", "배터리"]):
        return "전기전자"
    if any(k in name for k in ["자동차", "트레일러", "차량"]):
        return "운수장비"
    if any(k in name for k in ["선박", "항공", "운송장비"]):
        return "운수장비"
    if any(k in name for k in ["화학", "플라스틱", "고무"]):
        return "화학"
    if any(k in name for k in ["철강", "금속", "주조"]):
        return "철강금속"
    if any(k in name for k in ["기계", "장비"]):
        return "기계"
    if any(k in name for k in ["의료", "정밀", "광학"]):
        return "의료정밀"
    if any(k in name for k in ["건설", "토목"]):
        return "건설업"
    if any(k in name for k in ["통신", "소프트웨어", "컴퓨터", "IT", "인터넷", "데이터"]):
        return "통신업"
    if any(k in name for k in ["전기", "가스", "에너지", "발전"]):
        return "전기가스업"
    if any(k in name for k in ["운수", "항공", "창고", "물류"]):
        return "운수창고"
    if any(k in name for k in ["도매", "소매", "유통"]):
        return "유통업"
    if any(k in name for k in ["음식", "식품", "음료", "주류", "농"]):
        return "음식료품"
    if any(k in name for k in ["섬유", "의복", "의류"]):
        return "섬유의복"
    if any(k in name for k in ["종이", "목재", "가구"]):
        return "종이목재"
    if any(k in name for k in ["광물", "시멘트", "유리"]):
        return "비금속광물"
    if any(k in name for k in ["부동산", "임대"]):
        return "서비스업"

    return "서비스업"  # 분류 불가 → 서비스업으로 처리


def build_sectors_json(kospi_df: pd.DataFrame) -> dict:
    """DataFrame → sectors.json 구조 생성"""
    sectors = {}

    for _, row in kospi_df.iterrows():
        krx_sector = map_to_krx_sector(row["업종"])
        index_code = KRX_SECTORS.get(krx_sector, "")

        if krx_sector not in sectors:
            sectors[krx_sector] = {
                "index_code": index_code,
                "stocks": [],
            }

        sectors[krx_sector]["stocks"].append({
            "code": row["종목코드"],
            "name": row["회사명"],
        })

    # 섹터 내 종목 수 기준 정렬
    return dict(sorted(sectors.items(), key=lambda x: -len(x[1]["stocks"])))


def main():
    print("KIND에서 KOSPI 상장법인 목록 다운로드 중...")
    kospi_df = fetch_kospi_stocks()
    print(f"  → 유효 종목 수: {len(kospi_df)}개")

    # 매핑 안 된 업종 확인
    unmapped = set()
    for u in kospi_df["업종"].unique():
        if map_to_krx_sector(u) == "서비스업" and u not in KSIC_TO_KRX:
            unmapped.add(u)
    if unmapped:
        print(f"\n[주의] 서비스업으로 fallback된 업종 ({len(unmapped)}개):")
        for u in sorted(unmapped):
            count = len(kospi_df[kospi_df["업종"] == u])
            print(f"  {u}: {count}개")

    print("\nsectors.json 생성 중...")
    sectors = build_sectors_json(kospi_df)

    OUTPUT.parent.mkdir(exist_ok=True)
    with open(OUTPUT, "w", encoding="utf-8") as f:
        json.dump(sectors, f, ensure_ascii=False, indent=2)

    print(f"\n완료: {OUTPUT}")
    print(f"\n섹터별 종목 수:")
    for name, data in sectors.items():
        print(f"  {name} ({data['index_code']}): {len(data['stocks'])}개")


if __name__ == "__main__":
    main()
